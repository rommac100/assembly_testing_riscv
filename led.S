.global _start

.equ GPIO_CTRL_ADDR, 0x10012000
.equ GPIO_OUTPUT_EN_OFFSET, 0x08
.equ GPIO_OUTPUT_VAL_OFFSET, 0x0C
.equ GPIO_OUTPUT_XOR_OFFSET, 0x40
.equ GPIO_LED_ADDR, 0x00080000

_start:
	li t0, GPIO_CTRL_ADDR
	li t1, GPIO_LED_ADDR
	sw t1, GPIO_OUTPUT_EN_OFFSET(t0)
	sw t1, GPIO_OUTPUT_XOR_OFFSET(t0)
	sw t1, GPIO_OUTPUT_VAL_OFFSET(t0)
	


